package ud04_01;

public class VariablesNumericas {
	public static void main(String[] args) {
		int num1 = 20;
		int num2 = 2;

		System.out.println("La suma de " + num1 + " + " + num2 + " es: " + (num1 + num2));
		System.out.println("La resta de " + num1 + " - " + num2 + " es: " + (num1 - num2));
		System.out.println("La multiplicación de " + num1 + " x " + num2 + " es: " + (num1 * num2));
		System.out.println("La división entre " + num1 + " / " + num2 + " es: " + (num1 / num2));
		System.out.println("El módulo entre " + num1 + " / " + num2 + " es: " + (num1 % num2));
	}
}
